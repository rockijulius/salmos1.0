    private boolean validaCampos() {
        StringUtil util = StringUtil.getInstance();
        if (txtNome.getText().equals("") && txtDescricao.getText().equals("") && txtQuantidade.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Todos os campos são obrigatórios.", "CAMPOS OBRIGATÓRIOS",
                    JOptionPane.ERROR_MESSAGE);
            return false;

        } else if (util.isNullOrEmpty(txtNome.getText())) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Nome.", "CAMPOS OBRIGATÓRIOS",
                    JOptionPane.WARNING_MESSAGE);
            return false;

        } else if (util.isNullOrEmpty(txtDescricao.getText())) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Descricao", "CAMPOS OBRIGATÓRIOS",
                    JOptionPane.WARNING_MESSAGE);
            return false;

        } else if (util.isNullOrEmpty(txtQuantidade.getText())) {
            JOptionPane.showMessageDialog(null, "Preencha o campo Quantidade", "CAMPOS OBRIGATÓRIOS",
                    JOptionPane.WARNING_MESSAGE);
            return false;

        } 

        return true;
    }
