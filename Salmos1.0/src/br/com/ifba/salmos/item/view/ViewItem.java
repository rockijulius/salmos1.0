    private void btnAdicionarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAdicionarActionPerformed
        // TODO add your handling code here:
        
        Item item = new Item();
        
        int quantidade = Integer.parseInt(txtQuantidade.getText());
        int i;
        List<Item> verificarItem = FacadeInstance.getInstance().getAllItem();
        
        if(validaCampos() == true){
            item.setNome(txtNome.getText());
            item.setDescricao(txtDescricao.getText());
            item.setQuantidade(quantidade);
            
            for(i = 0; i < verificarItem.size(); i++){
                if(item.getNome().equals(verificarItem.get(i).getNome())){
                    JOptionPane.showMessageDialog(null, "Nome do item já existente", "Atenção", JOptionPane.WARNING_MESSAGE);
                    return;
                }
            }
        }
        
        FacadeInstance.getInstance().saveItem(item);
        
        JOptionPane.showMessageDialog(null, "Item adicionado ao estoque", "Parabens!!", JOptionPane.INFORMATION_MESSAGE);
        
        txtNome.setText(null);
        txtDescricao.setText(null);
        txtQuantidade.setText(null);
        
        this.itemLista = FacadeInstance.getInstance().getAllItem();
        this.atualizarTabelaItem(itemLista);
    }//GEN-LAST:event_btnAdicionarActionPerformed

