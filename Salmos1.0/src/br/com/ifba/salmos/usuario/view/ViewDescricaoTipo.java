    public void atualizarDescricao(List<TipoDeUsuario> tipoList){
        this.listaDescricao = new DefaultTableModel(null, new String [] {"Tipo de Usuario", "Descricao"});
        
        for(TipoDeUsuario tipos: tipoList){
            List<TipoDeUsuario> listaTipo = FacadeInstance.getInstance().getAllTipoDeUsuarios();
            listaDescricao.addRow(new Object [] {tipos.getNome(), tipos.getDescricao()});
        }
        
        this.tblDescricao.setModel(this.listaDescricao);
    }

